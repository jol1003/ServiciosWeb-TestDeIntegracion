package com.ransa.modelo;

public class TipoPedidoRansa {
	private int IdTipoPedido;
	private String NomTipoPedido;
	
	public int getIdTipoPedido() {
		return IdTipoPedido;
	}
	public void setIdTipoPedido(int idTipoPedido) {
		IdTipoPedido = idTipoPedido;
	}
	public String getNomTipoPedido() {
		return NomTipoPedido;
	}
	public void setNomTipoPedido(String nomTipoPedido) {
		NomTipoPedido = nomTipoPedido;
	}
	
	
}
